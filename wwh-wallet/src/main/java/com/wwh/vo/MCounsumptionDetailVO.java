package com.wwh.vo;

import java.math.BigDecimal;

/**
 * 消费分红详情vo
 * 
 * @author u1
 *
 */
public class MCounsumptionDetailVO {
	// 订单金额
	private BigDecimal orderAmount = new BigDecimal(0.00);
	// 创建日期
	private String createdDate;
	// 获得的金币
	private BigDecimal profitAmount = new BigDecimal(0.00);
	// 用户名
	private String userName;

	public BigDecimal getOrderAmount() {
		return orderAmount;
	}

	public void setOrderAmount(BigDecimal orderAmount) {
		this.orderAmount = orderAmount;
	}

	public String getCreatedDate() {
		return createdDate;
	}

	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}

	public BigDecimal getProfitAmount() {
		return profitAmount;
	}

	public void setProfitAmount(BigDecimal profitAmount) {
		this.profitAmount = profitAmount;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	@Override
	public String toString() {
		return "MCounsumptionDetailVO [orderAmount=" + orderAmount + ", createdDate=" + createdDate + ", profitAmount="
				+ profitAmount + ", userName=" + userName + "]";
	}

}
