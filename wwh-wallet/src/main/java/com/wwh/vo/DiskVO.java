package com.wwh.vo;

public class DiskVO extends BaseVO {

	/**
	 * @fieldName: serialVersionUID
	 * @fieldType: long
	 * @Description: TODO
	 */
	private static final long serialVersionUID = -7727299537147884881L;

	/**
	 * 盘序列
	 */
	private String diskSeq;
	/**
	 * 
	 */
	private String diskName;
	/**
	 * 盘状态
	 */
	private String diskStatus;
	/**
	 * 本盘父盘
	 */
	private String diskParentSeq;

	/**
	 * 本盘人数
	 */
	private Integer diskCounter;

	/**
	 * 盘类型
	 */
	private String diskType;

	/**
	 * 盘关系
	 */
	public DiskRelationVO diskRelationVO;

	/**
	 * 用于显示给用户的盘号——头信息
	 */
	private String diskHead;
	/**
	 * 用于显示给用户的盘号 ——序号信息
	 */
	private String diskTail;

	public String getDiskHead() {
		return diskHead;
	}

	public void setDiskHead(String diskHead) {
		this.diskHead = diskHead;
	}

	public String getDiskTail() {
		return diskTail;
	}

	public void setDiskTail(String diskTail) {
		this.diskTail = diskTail;
	}

	public String getDiskSeq() {
		return diskSeq;
	}

	public void setDiskSeq(String diskSeq) {
		this.diskSeq = diskSeq;
	}

	public String getDiskStatus() {
		return diskStatus;
	}

	public void setDiskStatus(String diskStatus) {
		this.diskStatus = diskStatus;
	}

	public String getDiskParentSeq() {
		return diskParentSeq;
	}

	public void setDiskParentSeq(String diskParentSeq) {
		this.diskParentSeq = diskParentSeq;
	}

	public Integer getDiskCounter() {
		return diskCounter;
	}

	public void setDiskCounter(Integer diskCounter) {
		this.diskCounter = diskCounter;
	}

	public String getDiskType() {
		return diskType;
	}

	public void setDiskType(String diskType) {
		this.diskType = diskType;
	}

	public DiskRelationVO getDiskRelationVO() {
		return diskRelationVO;
	}

	public void setDiskRelationVO(DiskRelationVO diskRelationVO) {
		this.diskRelationVO = diskRelationVO;
	}

	public String getDiskName() {
		return diskName;
	}

	public void setDiskName(String diskName) {
		this.diskName = diskName;
	}

	@Override
	public String toString() {
		return "DiskVO [diskSeq=" + diskSeq + ", diskName=" + diskName + ", diskStatus=" + diskStatus + ", diskParentSeq=" + diskParentSeq + ", diskCounter="
				+ diskCounter + ", diskType=" + diskType + ", diskRelationVO=" + diskRelationVO + ", diskHead=" + diskHead + ", diskTail=" + diskTail + "]";
	}

}
