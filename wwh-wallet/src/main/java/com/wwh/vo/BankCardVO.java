package com.wwh.vo;

public class BankCardVO extends BaseVO{

	private static final long serialVersionUID = 6608167548997652436L;

	private String bankCardId;     //银行卡id
	
	private String bankCardName;    //银行卡名称
	
	private String bankCardNumber;  //银行卡号
	
	private String bankCardType;   //银行卡类型
	
	private String bankName;     //银行名称
	
	private String phoneNumber;   //电话号码
	
	private String realName;   //银行卡用户的真是姓名
	
	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getBankCardId() {
		return bankCardId;
	}

	public void setBankCardId(String bankCardId) {
		this.bankCardId = bankCardId;
	}

	public String getBankCardName() {
		return bankCardName;
	}

	public void setBankCardName(String bankCardName) {
		this.bankCardName = bankCardName;
	}

	public String getBankCardNumber() {
		return bankCardNumber;
	}

	public void setBankCardNumber(String bankCardNumber) {
		this.bankCardNumber = bankCardNumber;
	}

	public String getBankCardType() {
		return bankCardType;
	}

	public void setBankCardType(String bankCardType) {
		this.bankCardType = bankCardType;
	}

	public String getBankName() {
		return bankName;
	}

	public void setBankName(String bankName) {
		this.bankName = bankName;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	
}
