package com.wwh.vo;

import org.springframework.boot.context.properties.ConfigurationProperties;

@ConfigurationProperties(prefix = "wallet",locations = "classpath:wallet.properties")
public class WalletVO {
	private String verifyTokenUrl;
	private String nonlandingUrl;
	public String getVerifyTokenUrl() {
		return verifyTokenUrl;
	}
	public void setVerifyTokenUrl(String verifyTokenUrl) {
		this.verifyTokenUrl = verifyTokenUrl;
	}
	public String getNonlandingUrl() {
		return nonlandingUrl;
	}
	public void setNonlandingUrl(String nonlandingUrl) {
		this.nonlandingUrl = nonlandingUrl;
	}
}
